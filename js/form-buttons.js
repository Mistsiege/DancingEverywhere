$( "#cdisc" ).click(function() { // On Clicking the Choose Disclaimer Radio Button
  $( "#fdisc" ).switchClass( "fhidden", "fvisible", 1000, "easeInOutQuad" ); // Show Disclaimer
  $( "#fscene" ).switchClass( "fvisible", "fhidden", 1000, "easeInOutQuad" ); // Hide Scene Form
  $( "#fstudio" ).switchClass( "fvisible", "fhidden", 1000, "easeInOutQuad" ); // Hide Studio Form
  $( "#fprivate" ).switchClass( "fvisible", "fhidden", 1000, "easeInOutQuad" ); // Hide Private Instructor Form
  $( "#fcause" ).switchClass( "fvisible", "fhidden", 1000, "easeInOutQuad" ); // Hide Cause Form
  $( "#fevent" ).switchClass( "fvisible", "fhidden", 1000, "easeInOutQuad" ); // Hide Event Form
  $( ".submit-form-container" ).switchClass( "tc-norm", "tc-scene", 1000, "easeInOutQuad" ); // Resize Submit Form Container
}); // End Disclaimer OnClick
